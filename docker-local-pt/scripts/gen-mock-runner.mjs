#!/usr/bin/env node
/**
 * Emit static-import k6 runner for one mock scenario.
 * Usage: node gen-mock-runner.mjs --suite Growin_Calendar --scenario BP001 --platform Web --variant original --out docker-local-pt/results/.runner.js
 */
import fs from 'node:fs';
import path from 'node:path';

function arg(name, def = '') {
  const i = process.argv.indexOf(`--${name}`);
  return i >= 0 ? process.argv[i + 1] : def;
}

const suite = arg('suite', 'Growin_PT_Dev[ToDo]');
const scenario = arg('scenario', 'BP001');
const platform = arg('platform', 'Web');
const variant = arg('variant', 'original');
const out = arg('out', 'docker-local-pt/results/.runner.js');
const prefix = variant === 'enchange' ? 'enchange_' : '';
const modulePath = `/workspace/Script/${suite}/${platform}/${prefix}${scenario}.js`;
const hasConfig = arg('has-config', 'false') === 'true';

const thresholdBlock = hasConfig
  ? `const thresholds = typeof buildThresholds === 'function' && BP_CONFIG ? buildThresholds(BP_CONFIG) : { http_req_failed: ['rate<0.5'], http_req_duration: ['p(95)<5000'] };`
  : `const thresholds = { http_req_failed: ['rate<0.5'], http_req_duration: ['p(95)<5000'] };`;

const importLine = hasConfig
  ? `import { ${scenario}, buildThresholds } from '${modulePath}';`
  : `import { ${scenario} } from '${modulePath}';`;

const vuUtilImport = `import { resolveVuCount, clampLocalVuCount } from '/workspace/docker-local-pt/scripts/k6-env-utils.js';`;

const body = `// AUTO-GENERATED — do not edit. Regenerated by gen-mock-runner.mjs
${importLine}
${vuUtilImport}

const SUITE = ${JSON.stringify(suite)};
const SCENARIO = ${JSON.stringify(scenario)};
const PLATFORM = ${JSON.stringify(platform)};
const VARIANT = ${JSON.stringify(variant)};
const BASE_URL = __ENV.BASE_URL || 'http://mock-api:8080';

let BP_CONFIG = null;
if (__ENV.BP_CONFIG) {
  try { BP_CONFIG = JSON.parse(__ENV.BP_CONFIG); }
  catch (e) { throw new Error('BP_CONFIG invalid JSON'); }
}

${thresholdBlock}

const runTags = {};
if (__ENV.RUN_ID) runTags.run_id = __ENV.RUN_ID;
if (__ENV.SCENARIO) runTags.scenario = __ENV.SCENARIO;
if (__ENV.PLATFORM) runTags.platform = __ENV.PLATFORM;
if (__ENV.VARIANT) runTags.variant = __ENV.VARIANT;
if (__ENV.RUNBY) runTags.runby = __ENV.RUNBY;

const vus = clampLocalVuCount(resolveVuCount());

export const options = {
  vus,
  duration: __ENV.DURATION || '30s',
  thresholds,
  tags: runTags,
  setupTimeout: '120s',
  teardownTimeout: '60s',
};

export function setup() {
  const total = options.vus;
  const vuMapping = {};
  const tokens = {};
  for (let i = 1; i <= total; i++) {
    const userKey = 'local-' + i;
    vuMapping[i] = { userKey, credentials: { email: userKey + '@example.test', password: 'mock' } };
    tokens[userKey] = { token: 'mock-token-' + i, pin_token: 'mock-pin-' + i, email: userKey + '@example.test' };
  }
  return { base_url: BASE_URL, tokens, vuMapping, BP_CONFIG };
}

export default function (data) { return ${scenario}(data); }

function safeNum(o, path, def) {
  try { for (const k of path) { o = o[k]; if (o == null) return def; } return o == null ? def : o; }
  catch (e) { return def; }
}
function collectCustomMetrics(metrics) {
  const out = {};
  for (const [name, m] of Object.entries(metrics || {})) {
    if (/^(duration_|error_rate_|sample_|waiting_)/.test(name)) {
      out[name] = { count: safeNum(m, ['values', 'count'], 0), p95: safeNum(m, ['values', 'p(95)'], 0) };
    }
  }
  return out;
}

export function handleSummary(data) {
  const stamp = __ENV.MOCK_RESULT_BASENAME || 'summary';
  const out = {
    mode: 'LOCAL_DOCKER_MOCK',
    suite: SUITE, scenario: SCENARIO, platform: PLATFORM, variant: VARIANT,
    module: ${JSON.stringify(modulePath)}, base_url: BASE_URL,
    metricCount: Object.keys(data.metrics || {}).length,
    iterations: safeNum(data, ['metrics', 'iterations', 'values', 'count'], 0),
    http_reqs: safeNum(data, ['metrics', 'http_reqs', 'values', 'count'], 0),
    http_req_failed_rate: safeNum(data, ['metrics', 'http_req_failed', 'values', 'rate'], 0),
    checks_passed: safeNum(data, ['metrics', 'checks', 'values', 'passes'], 0),
    checks_failed: safeNum(data, ['metrics', 'checks', 'values', 'fails'], 0),
    http_req_duration_p95: safeNum(data, ['metrics', 'http_req_duration', 'values', 'p(95)'], 0),
    custom_metrics: collectCustomMetrics(data.metrics),
    timestamp: new Date().toISOString(),
  };
  return {
    stdout: JSON.stringify(out, null, 2) + '\\n',
    ['/results/' + stamp + '.json']: JSON.stringify(out, null, 2),
    '/results/summary.json': JSON.stringify(out, null, 2),
  };
}
`;

fs.mkdirSync(path.dirname(out), { recursive: true });
fs.writeFileSync(out, body);
console.log(out);
